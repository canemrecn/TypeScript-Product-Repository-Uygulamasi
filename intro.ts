function greeter(name) { // "greeter" adında bir fonksiyon tanımlanır. Bu fonksiyon, bir isim parametresi alır.
    console.log('Hello ' + name); // Konsola "Hello" ile başlayan ve verilen ismi içeren bir ileti yazdırılır.
}

let user = 'Sadık'; // "user" adında bir değişken oluşturulur ve "Sadık" değeri atanır. Değişkenin let anahtar kelimesiyle tanımlandığına dikkat edilmelidir.

greeter(user); // "greeter" fonksiyonu çağrılır ve "user" değişkeni fonksiyona argüman olarak geçirilir.
